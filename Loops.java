public class Loops 
{  
public static void main(String[] args)   
{  
   int i,num=7,flag=0;
   for(i=2;i<num;i++) {

   if(num%i==0) {
	   flag=1;
	  
	   }}
   if(flag==1) {
	   System.out.println("is not prime");
   }
   else {
	   System.out.println("7 is prime");
   }
   }
   
   
}  
